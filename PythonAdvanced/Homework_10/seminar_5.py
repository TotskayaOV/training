class Animal:

    def __init__(self, name: str, age: int):
        self.name = name
        self.age = age
        self.spec = None

    def get_spec(self):
        return self.spec


class Dog(Animal):

    def __init__(self, name: str, age: int, spec):
        super().__init__(name, age)
        self.spec = spec


class Cat(Animal):

    def __init__(self, name: str, age: int, spec):
        super().__init__(name, age)
        self.spec = spec


class Bird(Animal):

    def __init__(self, name: str, age: int, spec):
        super().__init__(name, age)
        self.spec = spec

if __name__ == '__main__':
    dog_1 = Dog('Bim', 5, '–ª–∞–µ—Ç')
    cat_1 = Cat('Marusia', 3, '—Å–ø–∏—Ç')
    bird_1 = Bird('Jeck', 1, '–ø–æ–µ—Ç')

    for pet in [dog_1, cat_1, bird_1]:
        print(f'{pet.name} {pet.get_spec()}')
